df = pd.DataFrame(data)


# TF-IDF vectorizer

tfidf = TfidfVectorizer(stop_words='english')
tfidf_matrix = tfidf.fit_transform(df['content'])


# Recommendation Function

def get_recommendations_by_keyword(keyword, tfidf=tfidf, tfidf_matrix=tfidf_matrix, df=df):
    if not keyword.strip():
        return "Please enter a keyword or phrase."

    keyword_vec = tfidf.transform([keyword])
    cosine_sim = linear_kernel(keyword_vec, tfidf_matrix).flatten()
    top_indices = cosine_sim.argsort()[::-1][:5]

    recommendations = []
    for idx in top_indices:
        recommendations.append({
            'id': df.iloc[idx]['id'],
            'title': df.iloc[idx]['title'],
            'score': round(cosine_sim[idx], 3)
        })
    return recommendations


# Streamlit UI

st.title("Keyword-Based Recommendation System ðŸ§ ")

st.header("Type a keyword or phrase to get recommendations")
keyword_input = st.text_input("Enter keyword or phrase here:")

if st.button("Get Recommendations"):
    results = get_recommendations_by_keyword(keyword_input)

    if isinstance(results, str):
        st.warning(results)
    else:
        st.success(f"Top recommendations for '{keyword_input}':")
        for i, rec in enumerate(results, 1):
            st.write(f"{i}. {rec['id']} - {rec['title']} (Score: {rec['score']})")
